//// Execution context is nothing but a container or box where whole js code is executed

/// Execution context is divided into two components 
//// 1. Memory component -->> Here variable and its value is stored as a json i.e.., key and value pairs. Memory component is also callled as variable Environment
//// 2. Code component -->> Here code is running one by one line . Code component is also called as Thread of Execution.

///// Javascript is synchronous single threaded language.Synchronous Single threaded means JS can execute single function at a time with a definite order.Js code will not go on the next line until and unless previous code executed.
